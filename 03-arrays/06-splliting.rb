
# Spliting

arr = "where are you now ".split(" ") # splits a string into three element arary
print arr

arr = "where,are,you,now ".split(",")
print arr

arr = "where are you now ".split("")
print arr

# Perhaps the most common use of split is with no arguments; 
# in this case, the default behavior is to split on whitespace (such as spaces, tabs, or newlines):

print "ant bat cat".split

print "ant     bat\t\tcat\n    duck".split
