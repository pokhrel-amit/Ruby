# puts vs prints vs p

# puts
# It adds a new line for every puts statement.
# converts everything into string
# call the to_s method on the object
# always return nil

puts "Hello Universe !"


# print
# it doesn't add any new line
# always return nil
# call the to_s method on the object

print "all"
print "code"
print "is "
print "written"
print "in"
print "one"
print "line"

# p
# calls the 'inspect' method on the object

p "is this a string"